/*
In this module, we will see Kotlin arithmetic operations
 */

fun main() {

    //First Way
    println(1 + 2)
    println(2 - 2)
    println(3 * 2)
    println(4 / 2)

    //Second Way
    println(1.plus(2)) //Addition
    println(2.times(2)) //Multiplication
    println(4.div(2)) //Division
}